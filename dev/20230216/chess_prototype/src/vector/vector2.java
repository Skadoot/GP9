package vector;

public class vector2
{
    //x coordinate for the board.
    public int x;

    //y coordinate for the board.
    public int y;

    //default constructor with no parameters.
    public vector2()
    {
        this.x = 0;
        this.y = 0;
    }

    //constructor with parameters.
    public vector2(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}